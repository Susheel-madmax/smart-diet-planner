-- ============================================================
--  STEP 1: SEQUENCES (replace GENERATED BY DEFAULT AS IDENTITY)
--  START WITH 1, INCREMENT BY 1, no upper limit, cached for speed
-- ============================================================

CREATE SEQUENCE seq_users
    START WITH 1
    INCREMENT BY 1
    NOMAXVALUE
    NOCYCLE
    CACHE 20;

CREATE SEQUENCE seq_diet_plan
    START WITH 1
    INCREMENT BY 1
    NOMAXVALUE
    NOCYCLE
    CACHE 20;

CREATE SEQUENCE seq_progress
    START WITH 1
    INCREMENT BY 1
    NOMAXVALUE
    NOCYCLE
    CACHE 20;

CREATE SEQUENCE seq_streaks
    START WITH 1
    INCREMENT BY 1
    NOMAXVALUE
    NOCYCLE
    CACHE 20;

CREATE SEQUENCE seq_meals
    START WITH 1
    INCREMENT BY 1
    NOMAXVALUE
    NOCYCLE
    CACHE 20;

-- ============================================================
--  STEP 2: TABLES
--  Notes:
--   - NUMBER(p,s) used instead of FLOAT for Oracle 11g safety
--   - SYSDATE used for default dates (CURRENT_DATE is fine too)
--   - VARCHAR2 lengths kept identical to original schema
-- ============================================================

-- ── USERS ────────────────────────────────────────────────────
CREATE TABLE users (
    user_id     NUMBER          NOT NULL,
    name        VARCHAR2(100)   NOT NULL,
    email       VARCHAR2(150)   NOT NULL,
    password    VARCHAR2(255)   NOT NULL,
    age         NUMBER(3)       NOT NULL,
    height_cm   NUMBER(5,2)     NOT NULL,
    weight_kg   NUMBER(5,2)     NOT NULL,
    gender      CHAR(1),
    goal        VARCHAR2(20),
    bmr         NUMBER(7,2),
    target_cal  NUMBER(7,2),
    created_at  DATE            DEFAULT SYSDATE,
    -- Primary Key
    CONSTRAINT pk_users         PRIMARY KEY (user_id),
    -- Unique email
    CONSTRAINT uq_users_email   UNIQUE (email),
    -- Gender must be M or F
    CONSTRAINT ck_users_gender  CHECK (gender IN ('M', 'F')),
    -- Goal must be one of three values
    CONSTRAINT ck_users_goal    CHECK (goal IN ('loss', 'gain', 'maintain'))
);

-- ── DIET_PLAN ─────────────────────────────────────────────────
CREATE TABLE diet_plan (
    plan_id     NUMBER          NOT NULL,
    user_id     NUMBER,
    plan_date   DATE            DEFAULT SYSDATE,
    breakfast   VARCHAR2(500),
    lunch       VARCHAR2(500),
    dinner      VARCHAR2(500),
    total_cal   NUMBER(7,2),
    water_ml    NUMBER(5),
    -- Primary Key
    CONSTRAINT pk_diet_plan     PRIMARY KEY (plan_id),
    -- Foreign Key to users with cascade delete
    CONSTRAINT fk_diet_user     FOREIGN KEY (user_id)
                                REFERENCES users(user_id)
                                ON DELETE CASCADE
);

-- ── PROGRESS ──────────────────────────────────────────────────
CREATE TABLE progress (
    progress_id     NUMBER      NOT NULL,
    user_id         NUMBER,
    log_date        DATE        DEFAULT SYSDATE,
    weight_kg       NUMBER(5,2),
    calories_eaten  NUMBER(7,2),
    notes           VARCHAR2(300),
    -- Primary Key
    CONSTRAINT pk_progress      PRIMARY KEY (progress_id),
    -- Foreign Key to users with cascade delete
    CONSTRAINT fk_progress_user FOREIGN KEY (user_id)
                                REFERENCES users(user_id)
                                ON DELETE CASCADE
);

-- ── STREAKS ───────────────────────────────────────────────────
CREATE TABLE streaks (
    streak_id       NUMBER      NOT NULL,
    user_id         NUMBER,
    current_streak  NUMBER      DEFAULT 0,
    longest_streak  NUMBER      DEFAULT 0,
    last_log_date   DATE,
    badge_7         CHAR(1)     DEFAULT 'N',
    badge_14        CHAR(1)     DEFAULT 'N',
    badge_30        CHAR(1)     DEFAULT 'N',
    -- Primary Key
    CONSTRAINT pk_streaks       PRIMARY KEY (streak_id),
    -- Foreign Key to users with cascade delete
    CONSTRAINT fk_streaks_user  FOREIGN KEY (user_id)
                                REFERENCES users(user_id)
                                ON DELETE CASCADE,
    -- Badge values restricted to Y/N
    CONSTRAINT ck_badge_7       CHECK (badge_7  IN ('Y','N')),
    CONSTRAINT ck_badge_14      CHECK (badge_14 IN ('Y','N')),
    CONSTRAINT ck_badge_30      CHECK (badge_30 IN ('Y','N'))
);

-- ── MEALS (reference / lookup table) ─────────────────────────
CREATE TABLE meals (
    meal_id     NUMBER          NOT NULL,
    meal_type   VARCHAR2(20),
    name        VARCHAR2(200),
    calories    NUMBER(5),
    protein_g   NUMBER(5,2),
    carbs_g     NUMBER(5,2),
    fat_g       NUMBER(5,2),
    goal_type   VARCHAR2(20),
    -- Primary Key
    CONSTRAINT pk_meals         PRIMARY KEY (meal_id),
    -- Meal type restricted values
    CONSTRAINT ck_meals_type    CHECK (meal_type IN ('breakfast','lunch','dinner','snack'))
);

-- ============================================================
--  STEP 3: BEFORE INSERT TRIGGERS
--  Each trigger pulls the next sequence value into :NEW.pk_col
--  so that INSERT statements do not need to specify the PK.
--  Oracle 11g uses the "FOR EACH ROW" trigger pattern.
-- ============================================================

-- ── Trigger: users ───────────────────────────────────────────
CREATE OR REPLACE TRIGGER trg_users_bi
    BEFORE INSERT ON users
    FOR EACH ROW
BEGIN
    -- Only assign a new ID if one was not supplied by the caller
    IF :NEW.user_id IS NULL THEN
        SELECT seq_users.NEXTVAL INTO :NEW.user_id FROM DUAL;
    END IF;
END;
/

-- ── Trigger: diet_plan ───────────────────────────────────────
CREATE OR REPLACE TRIGGER trg_diet_plan_bi
    BEFORE INSERT ON diet_plan
    FOR EACH ROW
BEGIN
    IF :NEW.plan_id IS NULL THEN
        SELECT seq_diet_plan.NEXTVAL INTO :NEW.plan_id FROM DUAL;
    END IF;
END;
/

-- ── Trigger: progress ────────────────────────────────────────
CREATE OR REPLACE TRIGGER trg_progress_bi
    BEFORE INSERT ON progress
    FOR EACH ROW
BEGIN
    IF :NEW.progress_id IS NULL THEN
        SELECT seq_progress.NEXTVAL INTO :NEW.progress_id FROM DUAL;
    END IF;
END;
/

-- ── Trigger: streaks ─────────────────────────────────────────
CREATE OR REPLACE TRIGGER trg_streaks_bi
    BEFORE INSERT ON streaks
    FOR EACH ROW
BEGIN
    IF :NEW.streak_id IS NULL THEN
        SELECT seq_streaks.NEXTVAL INTO :NEW.streak_id FROM DUAL;
    END IF;
END;
/

-- ── Trigger: meals ───────────────────────────────────────────
CREATE OR REPLACE TRIGGER trg_meals_bi
    BEFORE INSERT ON meals
    FOR EACH ROW
BEGIN
    IF :NEW.meal_id IS NULL THEN
        SELECT seq_meals.NEXTVAL INTO :NEW.meal_id FROM DUAL;
    END IF;
END;
/

