--  Smart Diet Planner -- Oracle 11g XE Compatible Schema
--  Replaces IDENTITY columns with SEQUENCE + TRIGGER pattern
--  Compatible with: SQL*Plus, Oracle 11g XE
--  Run order: sequences -> tables -> triggers -> inserts
-- ============================================================

-- ============================================================
--  STEP 0: Clean up existing objects (safe re-run)
--  DROP order respects foreign key dependencies:
--  child tables first, then parent tables, then sequences
